# Код, который подсчитывает количество введенных четных чисел
# Вводим целые положительные числа и анализируем их
# Остановка ввода - любое неположительное (<=0) число

count = 0 # Счетчик

while True:
    numeric = int(input())
    if numeric <= 0:
        break

    if numeric % 2 ==0:
        count += 1

print("Количество четных чисел:", count)