"""
Модуль пакета geometry
Содержащий набор функция для подсчета:
* площади прямоугольника
* периметра прямоугольника
"""

def area(width:float, length:float):
    """
    description
    """
    return width * length

def perimeter(width:float, length:float):
    """
    description
    """
    return 2 * (width + length)
